//横向显示
void show_word(int x0, int y0, int w, int h, unsigned char word[][w / 8], int color)
{
  int i, j, k;
  for (i = 0; i < h; i++)
  {
    for (j = 0; j < w / 8; j++)
    {
      // 解析 word[i][j] 的8个bits位，为1就显示颜色，为0就不显示
      for (k = 7; k >= 0; k--)
      {
        if (word[i][j] & (1 << k))
        {
          // 画点
          Lcd_Draw_Point(j * 8 + (7 - k) + x0, i + y0, color);
        }
      }
    }
  }
}

